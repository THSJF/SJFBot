import java.util.List;

public class RoomInfo {

    public int code;
    public String message;
    public int ttl;
    public Data data;

    public class Data {

        public Room_info room_info;
        public Anchor_info anchor_info;
        public int id;
        public String title;
        public String location;
        public int position;
        public String pic;
        public String link;
        public int weight;
        public int room_id;
        public int up_id;
        public int parent_area_id;
        public int area_id;
        public int live_status;
        public int av_id;
    }

    public class Anchor_info {
        public Base_info base_info;
        public Live_info live_info;
        public Relation_info relation_info;
        public Medal_info medal_info;
    }

    public class Relation_info {
        public int attention;
    }

    public class Live_info {
        public int level;
        public int level_color;
        public int score;
        public int upgrade_score;
        public List<Integer> current;
        public List<Integer> next;
        public String rank;
    }

    public class Base_info {
        public String uname;
        public String face;
        public String gender;
    }

    public class Medal_info {
        public String medal_name;
        public int medal_id;
        public int fansclub;
    }

    public class Room_info {
        public int uid;
        public int room_id;
        public int short_id;
        public String title;
        public String cover;
        public String tags;
        public String background;
        public String description;
        public int live_status;
        public int live_start_time;
        public int live_screen_type;
        public int lock_status;
        public int lock_time;
        public int hidden_status;
        public int hidden_time;
        public int area_id;
        public String area_name;
        public int parent_area_id;
        public String parent_area_name;
        public String keyframe;
        public int special_type;
        public String up_session;
        public int pk_status;
        public boolean is_studio;
        public int on_voice_join;
        public int online;
    }

    @Override
    public String toString() {        
        return String.format("主播:%s\n%s\n分区:%s-%s\n标签:%s",
                             data.anchor_info.base_info.uname,
                             data.room_info.live_status == 1 ? "标题:" + data.room_info.title : "未直播",
                             data.room_info.parent_area_name,
                             data.room_info.area_name,
                             data.room_info.tags);
    }
}
